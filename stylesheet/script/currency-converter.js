// Function to convert currency
function convertCurrency() {
    const sourceCurrencyCode = document.getElementById("sourceCurrency").value;
    const targetCurrencyCode = document.getElementById("targetCurrency").value;
    const amount = parseFloat(document.getElementById("amount").value);

    if (isNaN(amount) || amount < 1 || amount > 100000) {
        alert("Please enter a valid fuel amount between 1 and 100000.");
        return;
    }

    const sourceRate = currencyData[sourceCurrencyCode].rate;
    const targetRate = currencyData[targetCurrencyCode].rate;

    const convertedAmount = (amount / sourceRate) * targetRate;

    // Additional information
    const exchangeRateInfo = `Current Exchange Rate: 1 ${sourceCurrencyCode} = ${targetRate.toFixed(5)} ${targetCurrencyCode}`;

    // Calculate the current timestamp
    const timestamp = new Date();
    const timestampInfo = `Calculation Timestamp: ${formatTimestamp(timestamp)}`;

    // Display the result with additional information
    document.getElementById("result").innerHTML = `
    ${exchangeRateInfo}<br>
    ${timestampInfo}<br>
    Amount of transaction ${amount} ${sourceCurrencyCode}: ${convertedAmount.toFixed(2)} ${targetCurrencyCode}
    `;
}

// Function to format timestamp
function formatTimestamp(timestamp) {
    const options = { weekday: 'long', year: 'numeric', month: 'long', day: 'numeric', hour: 'numeric', minute: 'numeric', second: 'numeric', timeZoneName: 'short' };
    return timestamp.toLocaleDateString('en-US', options);
}

